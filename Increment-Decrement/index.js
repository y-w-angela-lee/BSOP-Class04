//Incrementing

var number = 0;
console.log(number); //0

    //longer version
number = number + 1;
console.log(number); //1 (0+1)

    //shorterhand version
number += 1;
console.log(number); //2 (1+1)

    //even shorter version, only increases by 1
number++;
console.log(number); //3 (2+1)

var i = 0;
//note putting the ++ post vs. pre 
console.log(++i); //1
console.log(i); //1
console.log(i++)//1
console.log(i);//2

//Decrementing

var value = 100;
console.log(value); // 100

value = value - 1;
console.log(value); //99 (100-1)

value -= 1;
console.log(value); //98 (99-1)

value--;
console.log(value); //97 (98-1), only decreases by 1

